    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:

        // Fetch the selected row index
        int index = jTable1.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(null, "Must select a row from the table for updating...");
            return;
        }

        // Get the ID of the selected singer from the first column
        String id =  (String) jTable1.getValueAt(index, 0);

        // Get the updated singer name from the text field
        String updatedSingerName = jTextFeildSingerName.getText();

        // Update the singer name in the database
        try {
            
            boolean isUpdated = singerRepo.update(new Singer(id,updatedSingerName));
            
           
            if(isUpdated){
            // Display success message
            JOptionPane.showMessageDialog(null, "Singer updated successfully");
             // Refresh the table to reflect the changes
            showSingers();

            }
           
            // Clear the text field
            jTextFeildSingerName.setText("");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error updating singer: " + e.getMessage());
        }


    }//GEN-LAST:event_jLabel5MouseClicked

    private void showSingers() {
        try {
            
            
            ArrayList<Singer>  list = singerRepo.getSingers();
            
            // Create a DefaultTableModel to hold the data for the jTable1
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Clear existing data from the table
            model.setRowCount(0);

            // Iterate through the result set and add each singer to the table
            for(Singer singer :list) {
                Object[] row = {singer.getId(), singer.getName()};
                model.addRow(row);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error fetching singers: " + e.getMessage());
        }
    }


