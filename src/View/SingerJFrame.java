    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        // Fetch the selected row index
        int index = jTable1.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(null, "Must select a row from the table for deleting...");
            return;
        }

        // Get the ID of the selected singer from the first column
        String id =  (String) jTable1.getValueAt(index, 0);

        // Confirm deletion
        int confirmDialog = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this singer?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirmDialog == JOptionPane.YES_OPTION) {
            // Delete the singer from the database
            try {
                
                boolean isDeleted = singerRepo.Delete(id);
              

                if(isDeleted){    // Display success message
                JOptionPane.showMessageDialog(null, "Singer deleted successfully");

                // Refresh the table to reflect the changes
                showSingers();
                jTextFeildSingerName.setText("");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error deleting singer: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jLabel6MouseClicked

